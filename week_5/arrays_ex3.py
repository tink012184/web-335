""" String Array of Composers """
composers = ["Beethoven", "Bach", "Mozart", "Brahms"]

for composer in composers:
    if composer == "Mozart":
        print("Wolfgang Amadeus Mozart was one of the greatest composers of Western Classical music!")
    elif composer == "Bach":
        print("Johann Sebastian Bach was a German composer!")
    else:
        print(composer)
        